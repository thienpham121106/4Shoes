function renderNewShoes() {
  const container = document.querySelector(".container-product");
  let newShoesProduct = "";
  newShoes.forEach(n => {
    newShoesProduct += `
            <div class="product"  onclick="viewProductDetail(${n.id})">
            <img src="${n.image}" alt="">
            <p class="product-name">${n.name}</p>
            <p class="product-price">${n.price.toLocaleString("vi-VN") + "đ"}</p>
        </div>
    `
  });
  container.innerHTML = newShoesProduct;
}

function renderBestSeller() {
  const container = document.querySelector(".best-seller-product");
  let bestSellerProduct = "";
  bestSeller.forEach(n => {
    bestSellerProduct += `
            <div class="product"  onclick="viewProductDetail(${n.id})">
            <img src="${n.image}" alt="">
            <p class="product-name">${n.name}</p>
            <p class="product-price">${n.price.toLocaleString("vi-VN") + "đ"}</p>
        </div>
    `
  });
  container.innerHTML = bestSellerProduct;
}

localStorage.setItem("allProducts", JSON.stringify([...newShoes, ...bestSeller]));


window.addEventListener("load", function() {
  renderNewShoes();
  renderBestSeller();
});


function viewProductDetail(id) {
  window.location.href = `productdetail.html?id=${id}`;
}





